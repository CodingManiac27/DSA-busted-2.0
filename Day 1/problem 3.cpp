#include <iostream>
using namespace std;

int main() {
    long long X, Y;
    cin >> X >> Y;

    // Output the sum, product, and difference of X and Y
    cout << X << " + " << Y << " = " << X + Y << endl;
    cout << X << " * " << Y << " = " << X * Y << endl;
    cout << X << " - " << Y << " = " << X - Y << endl;

    return 0;
}
